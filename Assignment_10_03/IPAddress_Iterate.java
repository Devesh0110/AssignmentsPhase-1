
public class IPAddress_Iterate {
public static void main(String args[]) {
	String ip[]=new String[3];
	ip[0]="153.1999383746484";
	ip[1]="153.1999383746489";
	ip[2]="153.1999383746477";
	System.out.println("Iterating through the String array:-");
	for(String i:ip) {
		System.out.println(i);
	}
}
}
